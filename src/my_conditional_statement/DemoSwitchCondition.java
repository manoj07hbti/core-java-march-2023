package my_conditional_statement;

public class DemoSwitchCondition {
    public static void main(String[] args) {
        int day=4;
        switch (day){
            case 1:
                System.out.println("Day is January");
                break;
            case 2:
                System.out.println("Day is February");
                break;
            case 3:
                System.out.println("Day is March");
                break;
            case 4:
                System.out.println("Day is April");
                break;
            case 5:
                System.out.println("Day is May");
                break;
            case 6:
                System.out.println("Day is june");
                break;
            case 7:
                System.out.println("Day is July");
                break;
            case 8:
                System.out.println("Day is August");
                break;
            case 9:
                System.out.println("Day is September");
                break;
            case 10:
                System.out.println("Day is October");
                break;
            case 11:
                System.out.println("Day is November");
                break;
            case 12:
                System.out.println("Day is December");
                break;
        }
    }
}
