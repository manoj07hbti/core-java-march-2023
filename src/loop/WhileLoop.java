package loop;

public class WhileLoop {

    public static void main(String[] args) {


        //increment while loops
        int i=1;
        while (i<=10){
            System.out.println("print while loop"+i);
            i++;

        }
        // decrement while loops

        int j=20;
        while (j>0){
            System.out.println("print decrement while loop.."+j);
            j--;
        }
    }
}
