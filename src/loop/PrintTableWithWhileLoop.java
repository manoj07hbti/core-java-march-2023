package loop;

public class PrintTableWithWhileLoop {

    public static void main(String[] args) {

        int i =1;
        int table=8;

        while ( i<=10){
            System.out.println("print table of 8 * "+ i +" = "+table*i);
            i++;
        }

        int j = 1;
        int a =90;
        while (j<=10){
            System.out.println(" print table of 90 * "+ j + " = "+a*j);
            j++;
        }

    }
}
