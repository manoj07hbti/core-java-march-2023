package loop;

public class ForLoop {

    public static void main(String[] args) {

        // increment for loop

        for (int i = 1; i <= 10; i++) {
            System.out.println("print for loops......" + i);
        }

        //decrement or loop



        for (int i=5;i>0;i--){

            System.out.println(" print decrement for loop.." + i);

        }
    }
}