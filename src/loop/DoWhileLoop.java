public class DoWhileLoop {

    public static void main(String[] args) {
   // increment with do while
        int i=1;
        do {
            System.out.println("print do while loop...."+i);
            i++;
        }  while (i <= 10) ;


        //decrement with do while

        int j = 10;

        do{
            System.out.println("print decrement with do while loop..."+j);
            j--;
        }while (j>0);

    }
}
