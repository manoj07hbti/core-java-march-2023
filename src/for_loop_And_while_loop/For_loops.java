package for_loop_And_while_loop;

public class For_loops {
    public static void main(String[] args) {

        //Increment for loops

         for(int k=1; k<=8; k++){
             System.out.println("my name is gaurav"+k);
         }


         for (int l=4; l<=20; l++){
             System.out.println("hello mom"+l);
         }


         for (int o=3; o<=7; o++){
             System.out.println("housekeeper"+o);
         }


         for (int kl=6; kl<=17; kl++){
             System.out.println("sony tv"+kl);
         }


         for (int pinky=19; pinky<=24; pinky++){
             System.out.println("pinky is gud person"+pinky);
         }

         for (int h=40; h<=60; h++){
             System.out.println("dad"+h);
         }



         //Decrement For loops

        for (int y=34; y>15; y--){
            System.out.println("girls"+y);
        }

        for (int chor=45; chor>22; chor--){
            System.out.println("thief"+chor);
        }

        for (int u=98; u>90; u--){
            System.out.println("u my life "+u);
        }

        for (int m=100; m>98; m--){
            System.out.println("Reverse"+m);
        }

        for (int s=56; s>45; s--){
            System.out.println("print "+s);
        }



    }
}
