package for_loop_And_while_loop;

public class assigment_table_3_6_8_10 {
    public static void main(String[] args) {

        // table assigment

        int table_number=3;
        for (int k=1; k<=10; k++){
            System.out.println("3x"+k+"="+table_number*k);
        }

        int proxy=6;
        for (int s=1; s<=10; s++){
            System.out.println("6X"+s+"="+proxy*s);
        }

        int Number=8;
        for (int D=1; D<=10; D++){
            System.out.println("8x"+D+"="+Number*D);
        }

        int True=10;
        for (int n=1; n<=10; n++){
            System.out.println("10x"+n+"="+True*n);
        }


        // Assigment while loops

        int ketan=3;
        int k=1;
        while (k<=10){
            System.out.println("3x"+k+"="+ketan*k);
            k++;
        }

        int mintu=6;
        int u=1;
        while (u<=10){
            System.out.println("6x"+u+"="+mintu*u);
            u++;
        }

        int roddy=8;
        int t=1;
        while (t<=10){
            System.out.println("8x"+t+"="+roddy*t);
            t++;
        }

        int fool=10;
        int a=1;
        while (a<=10){
            System.out.println("10x"+a+"="+fool*a);
            a++;
        }


    }

}
