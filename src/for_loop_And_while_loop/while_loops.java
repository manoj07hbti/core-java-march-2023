package for_loop_And_while_loop;

public class while_loops {

    public static void main(String[] args) {

        // while loops increment

        int k=4;
        while (k<=8){
            System.out.println("mom"+ k );
            k++;
        }

        int o=1;
        while (o<=10){
            System.out.println("loin"+o);
            o++;
        }

        int Mayank=18;
        while (Mayank<=20){
            System.out.println("Mayank pig person"+Mayank);
            Mayank++;
        }

        // Decrement while loops ;

        int p=4;
        while (p>1){
            System.out.println("rohit"+p);
            p--;
        }

        int prize=45;
        while (prize>30){
            System.out.println("shirt prize");
            prize--;
        }

        int booking=100;
        while (booking>95){
            System.out.println("show booking "+booking);
            booking--;
        }

        int lkj=87;
        while (lkj>=80){
            System.out.println("time"+lkj);
            lkj--;

        }

    }
}
