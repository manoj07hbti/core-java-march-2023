package variables;

public class DemoVariables {

    public static void main(String[] args) {

        // SYNTAX :  Datatype varName;

        String cityName; // variable declaration

        cityName="AGRA"; // we have assigned value AGRA to cityName

        System.out.println("Printing City Name:" +cityName);

        cityName="DELHI";

        System.out.println(" Printing variable after changing value : "+cityName);

        int age; // we have created integer variable

        age=23;

        System.out.println("Printing Age : " +age);

        age=24;
        System.out.println("Printing Age after changing it  : " +age);
        double salary;// we have created decimal variable

        salary=234323.34;
        System.out.println("Printing Salary: "+salary);

        salary=63322.567;
        System.out.println("Printing Salary after change : "+salary);

    }
}
