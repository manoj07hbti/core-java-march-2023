package loops;

public class DemoForLoop {

    public static void main(String[] args) {


        //SYNTAX: for (start_condition; expression; increment){
        //                 //CODE
        //           }
       // INCREMENT FOR LOOP
        for(int i=1; i<=5; i++){

            System.out.println("Hello World "+i);
        }

        //DECREMENT FOR LOOP

        for(int i=10; i>0 ; i--){

            System.out.println("Decrement for Loop "+i);
        }

    }
}
