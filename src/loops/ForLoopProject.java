package loops;

public class ForLoopProject {
    public static void main(String[] args) {

        //Using For loop Printing 5 Table
        int number=5;
        for(int i=1; i<=10;i++){
            System.out.println("5x"+i+"="+number*i);
        }

        //Using For loop Printing 9 Table
        number=9;
        for (int j=1; j<=10; j++){
            System.out.println("9x"+j+"="+number*j);
        }

        //Using For loop Printing 10 Table
        number=10;
        for (int k=1;k<=10;k++){
            System.out.println("10x"+"="+number*k);
        }
    }
}
