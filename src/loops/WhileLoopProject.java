package loops;

public class WhileLoopProject {

    public static void main(String[] args) {

        //Using While loop Printing 5 Table

        int number=5;
        int i=1;
        while(i<=10){
            System.out.println("5x"+i+"="+number*i);
            i++;
        }

        //Using While loop Printing 7 Table

        int table=7;
        int j=1;
        while(j<=10){
            System.out.println("7x"+j+"="+table*j);
            j++;
        }

        //Using While loop Printing 10 Table
        int digit=10;
        int k=1;
        while(k<=10) {
            System.out.println("10x"+k+"="+digit*k);
            k++;
        }
    }
}
