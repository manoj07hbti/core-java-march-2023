package loops;

public class Assignment {

    public static void main(String[] args) {

        int number=2;

        for (int i=1; i<=10; i++){

            System.out.println("2 X "+i + " = "+number*i);

        }
        int j=1;
        while(j<=10){

            System.out.println(" While Loop 2 X "+j + " = "+number*j);
            j++;
        }

    }
}
