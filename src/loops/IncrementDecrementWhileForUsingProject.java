package loops;

public class IncrementDecrementWhileForUsingProject {
    public static void main(String[] args) {
        //Using For Loop Command & Making Increment project

        for (int i=1;i<=6; i++){
            System.out.println("Hello World This is Ajeet Chauhan"+i);
        }

        //Using For Loop Command & Making Decrement project

        for (int i=20; i>0; i--){
            System.out.println("Number"+"="+i);
        }

        //Using While Loop Command & Making Increment project

        int i=1;
        while(i<=7){
            System.out.println("Hello World This is Ajeet Chauhan"+i);
            i++;
        }

        //Using While Loop Command & Making Decrement project

       int j=10;
        while(j>=0){
            System.out.println("Hello World This is Ajeet Chauhan"+j);
            j--;
        }

    }
}
