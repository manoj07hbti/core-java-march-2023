package assignment_loops;

public class OddNumberLoops2DecAndEn {
    public static void main(String[]args){

        //SYNTAX: for (start_condition;expression; increment){
        //
        //              }
         // Increment for loop;

        for(int i=1;i<=50;i++){
            System.out.println("Hello Java"+i);

        }
        // Decrement for loop;
        //         //            // :
        for(int i=45;i>0; i--){
            System.out.println("Decrement for Java"+i);
        }
    }

}
