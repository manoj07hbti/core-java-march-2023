package myloops;

public class DoWhileLoopNumber {
    public static void main(String[] args) {

int a=2;
do{
    System.out.println("all even number 1 to 100: "+a);
    a+=2;
}while(a<100);
int b=1;
System.out.println("Odd numbers");
do{
    System.out.println("all odd number 1 to 100: "+b);
    b+=2;
}while(b<100);

        }
    }




